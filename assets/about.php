                <h3>About Us</h3>
                <p>
                    <img src="assets/images/anga.webp" alt="" class="img-rounded pull-right" width="300">
                    Children are one of the most beautiful creatures on earth – they are full of joy, energy and curiosity. They constantly explore everything around them and in the process learn many things. The process of exploration and learning in early childhood is crucial to the child’s development. Research studies show that 50% of the total intellectual development of a child is completed by the time he is four years old. Obviously, much care must be provided to the child during its first few years.
                </p>
                <p>It is the responsibility of the parents and the society to provide an enriching environment for children, so as to ensure proper development of their innate talents and skills to the maximum possible extent.</p>
                <h3>Our Achievements</h3>
                <strong>Nutrition including Supplementary Nutrition</strong>
                <p>This includes supplementary feeding and growth monitoring; and prophylaxis against vitamin A deficiency and control of nutritional anaemia. All families in the community are surveyed, to identify children below the age of six and pregnant & nursing mothers. They avail of supplementary feeding support for 300 days in a year. By providing supplementary feeding, the Anganwadi attempts to bridge the caloric gap between the national recommended and average intake of children and women in low income and disadvantaged communities. </p>
                <strong>Immunization</strong>
                <p>Immunization of pregnant women and infants protects children from six vaccine preventable diseases-poliomyelitis, diphtheria, pertussis, tetanus, tuberculosis and measles. These are major preventable causes of child mortality, disability, morbidity and related malnutrition. Immunization of pregnant women against tetanus also reduces maternal and neonatal mortality.</p>
                <strong>Health Check-ups</strong>
                <p>This includes health care of children less than six years of age, antenatal care of expectant mothers and postnatal care of nursing mothers. The various health services provided for children by anganwadi workers and Primary Health Centre (PHC) staff, include regular health check-ups, recording of weight, immunization, management of malnutrition, treatment of diarrhoea, de-worming and distribution of simple medicines etc.</p>
                <strong>Non-formal Pre-School Education (PSE) </strong>
                <p>The Non-formal Pre-school Education (PSE) component of the ICDS may well be considered the backbone of the ICDS programme, since all its services essentially converge at the anganwadi – a village courtyard. Anganwadi Centre (AWC) – a village courtyard – is the main platform for delivering of these services. These AWCs have been set up in every village in the country.</p>
